"""
triangle method provided by boot.dev
This creates the player as a triangle, inheriting from the CircleShape class
"""

import pygame
from circleshape import CircleShape
from constants import PLAYER_RADIUS, LINE_WIDTH, PLAYER_TURN_SPEED, PLAYER_SPEED, PLAYER_SHOOT_SPEED, PLAYER_SHOOT_COOLDOWN_SECONDS
from shot import Shot

# Base class for player objects
class Player(CircleShape):
    def __init__(self, x, y):
        super().__init__(x, y, PLAYER_RADIUS)
        self.rotation = 0
        self.cooldown = 0
    
    # in the Player class
    # Makes the player look like a triangle. The hitbox remains a circle from CircleShape
    def triangle(self):
        forward = pygame.Vector2(0, 1).rotate(self.rotation)
        right = pygame.Vector2(0, 1).rotate(self.rotation + 90) * self.radius / 1.5
        a = self.position + forward * self.radius
        b = self.position - forward * self.radius - right
        c = self.position - forward * self.radius + right
        return [a, b, c]
    
    """
        Cverride the draw method of CircleShape. It should take the screen object as a parameter, and call pygame.draw.polygon(). 
        It takes as inputs:
        - The screen object
        - A color (use "white")
        - A list of points (use the list returned by a call to the self.triangle() function)
        - A line width (use the one in your constants.py file)
    """
    def draw(self, screen):
        color = "white"
        pygame.draw.polygon(screen, color, self.triangle(), LINE_WIDTH)

    def rotate(self, dt):
        self.rotation += PLAYER_TURN_SPEED * dt

    def move(self, dt):
        unit_vector = pygame.Vector2(0, 1)
        rotated_vector = unit_vector.rotate(self.rotation)
        rotated_with_speed_vector = rotated_vector * PLAYER_SPEED * dt
        self.position += rotated_with_speed_vector

    def update(self, dt):
        self.cooldown -= dt
        
        keys = pygame.key.get_pressed()

        if keys[pygame.K_a]:
            self.rotate(-dt)
        if keys[pygame.K_d]:
            self.rotate(dt)
        if keys[pygame.K_w]:
            self.move(dt)
        if keys[pygame.K_s]:
            self.move(-dt)
        if keys[pygame.K_SPACE]:
            if self.cooldown > 0:
                pass
            else:
                self.shoot()
                self.cooldown = PLAYER_SHOOT_COOLDOWN_SECONDS
                

    def shoot(self):
        bullet = Shot(self.position.x, self.position.y)
        unit_vector = pygame.Vector2(0,1)
        bullet.velocity = unit_vector.rotate(self.rotation) * PLAYER_SHOOT_SPEED
